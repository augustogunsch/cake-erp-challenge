class Trainer:
    def __init__(self,
            id,
            nickname,
            first_name,
            last_name,
            email,
            password,
            team,
            pokemons_owned):
        self.id = id
        self.nickname = nickname
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.password = password
        self.team = team
        self.pokemons_owned = pokemons_owned
